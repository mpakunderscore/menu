export let getSortedIngredients = () => {
    let sortable = [];
    for (let id in ingredients) {
        sortable.push([id, ingredients[id]]);
    }
    sortable.sort(function(a, b) {
        return b[1] - a[1];
    });

    let final = []
    for (let j in sortable) {
        final.push({name: sortable[j][0], count: sortable[j][1]})
    }

    return final
}

